/**
 * && => And => e => Para Ser 'True' todos os valores precisam ser 'True'
 * || => Or => ou => Para Ser 'True' apenas um valor precisa ser 'True'
 * ! => Not => negação => Inverte o valor
 * ? => Ternario
 */

console.log(true && true); // And
console.log(true || false); // Or
console.log(!true); // Not
console.log(true ? "Verdadeiro" : "Falso"); // Ternario


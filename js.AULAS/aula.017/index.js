/**
 * Operadores de Comparação
 *
 * > Maior que
 * >= Maior ou igual
 * < Menor que
 * <= Menor ou igual
 * == Igual
 * === Idêntico
 * != Diferente
 * !== Diferente porem Idêntico
 */

const idade = 20;

console.log(idade > 18);
console.log(idade >= 18);
console.log(idade < 18);
console.log(idade <= 18);
console.log(idade == 18);
console.log(idade === 18);
console.log(idade != 18);
console.log(idade !== 18);
